    {
        $data = $request->validate(Tasks::validationRules());

        $data['user_id'] = Auth::user()->id;

        $task = Tasks::create($data);

        if ($request->ajax()){
            return [
                'result' => 'ok',
                'message' => 'Task kayÄ±t edildi.',
                'task' => $task
            ];
        }

        return redirect()->route('customers.edit', [task]);

    }

